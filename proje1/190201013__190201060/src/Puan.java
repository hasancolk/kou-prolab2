public class Puan extends Oyuncu{
    
    public Puan(){
        super();
    }
    
    public String puani_goster(Oyuncu oyuncu) {
        if (oyuncu instanceof Tembel) {
            return "Tembelin Puani : " + oyuncu.getPuan();         
        } 
        else {             
            return "Gozluklunun Puani : " + oyuncu.getPuan();         
        }     
    }
    
}
