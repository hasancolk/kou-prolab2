public class Yol {
    private int ilk_yol;
    private int son_yol;
    public Yol(){
        
    }
    public Yol(int ilk_yol, int son_yol) {
        this.ilk_yol = ilk_yol;
        this.son_yol = son_yol;
    }
    
    public int getIlk_yol() {
        return ilk_yol;
    }

    public void setIlk_yol(int ilk_yol) {
        this.ilk_yol = ilk_yol;
    }

    public int getSon_yol() {
        return son_yol;
    }

    public void setSon_yol(int son_yol) {
        this.son_yol = son_yol;
    }
    
}
